package chapter_6;

import java.util.HashSet;

public class Sudoku {

	public static void main(String[] args) {
		/*
		 * {'5','3','0','0','7','0','0','0','0'} {'6','0','0','1','9','5','0','0','0'}
		 * {'0','9','8','0','0','0','0','6','0'} {'8','0','0','0','6','0','0','0','3'}
		 * {'4','0','0','8','0','3','0','0','1'} {'7','0','0','0','2','0','0','0','6'}
		 * {'0','6','0','0','0','0','2','8','0'} {'0','0','0','4','1','9','0','0','5'}
		 * {'0','0','0','0','8','0','0','7','9'}
		 */

		char[][] sudoku = { { '5', '3', '0', '0', '7', '0', '0', '0', '0' },
				{ '6', '0', '0', '1', '9', '5', '0', '0', '0' }, { '0', '9', '8', '0', '0', '0', '0', '6', '0' },
				{ '8', '0', '0', '0', '6', '0', '0', '0', '3' }, { '4', '0', '0', '8', '0', '3', '0', '0', '1' },
				{ '7', '0', '0', '0', '2', '0', '0', '0', '6' }, { '0', '6', '0', '0', '0', '0', '2', '8', '0' },
				{ '0', '0', '0', '4', '1', '9', '0', '0', '5' }, { '0', '0', '0', '0', '8', '0', '0', '7', '9' } };
		System.out.println(validate(sudoku));

	}

	private static boolean validate(char[][] sudoku) {
		HashSet<String> seen = new HashSet<>();
		for (int i = 0; i < 9; ++i) {
			for (int j = 0; j < 9; ++j) {
				char number = sudoku[i][j];
				if (number != '0')
					if (!seen.add(number + " in row " + i) || !seen.add(number + " in column " + j)
							|| !seen.add(number + " in block " + i / 3 + "-" + j / 3))
						return false;
			}
		}
		return true;
	}
}
